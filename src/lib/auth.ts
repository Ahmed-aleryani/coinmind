// Server-side user management that handles both authenticated and anonymous users
export async function getUser() {
  try {
    const supabase = await createClient()
    const { data: { user }, error } = await supabase.auth.getUser()
    
    if (error) {
      logger.warn({ error: error.message }, 'Error getting authenticated user')
      return null
    }
    
    return user
  } catch (error) {
    logger.error({ error: error instanceof Error ? error.message : error }, 'Failed to get user')
    return null
  }
}

// Check if user is authenticated (not anonymous)
export async function isAuthenticated() {
  try {
    const user = await getUser()
    return user && user.app_metadata?.provider !== 'anonymous'
  } catch (error) {
    logger.error({ error: error instanceof Error ? error.message : error }, 'Failed to check authentication status')
    return false
  }
} 